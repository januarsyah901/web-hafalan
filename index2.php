<script>
    // Add some spooky interactivity
    document.addEventListener('DOMContentLoaded', function() {
        // Add random floating animations to table rows
        const rows = document.querySelectorAll('.spooky-row');
        rows.forEach((row, index) => {
            row.addEventListener('mouseenter', function() {
                this.style.transform = 'translateX(10px) scale(1.01)';
            });

            row.addEventListener('mouseleave', function() {
                this.style.transform = 'translateX(0) scale(1)';
            });
        });

        // Create floating particles
        function createParticle() {
            const particle = document.createElement('div');
            particle.style.position = 'fixed';
            particle.style.fontSize = '20px';
            particle.style.left = Math.random() * window.innerWidth + 'px';
            particle.style.top = window.innerHeight + 'px';
            particle.style.pointerEvents = 'none';
            particle.style.zIndex = '1';
            particle.textContent = ['🦇', '👻', '🕷️', '💀'][Math.floor(Math.random() * 4)];

            document.body.appendChild(particle);

            const animation = particle.animate([
                { transform: 'translateY(0px)', opacity: 1 },
                { transform: 'translateY(-' + (window.innerHeight + 100) + 'px)', opacity: 0 }
            ], {
                duration: Math.random() * 3000 + 2000,
                easing: 'linear'
            });

            animation.onfinish = () => particle.remove();
        }

        // Create particles periodically
        setInterval(createParticle, 3000);
    });
